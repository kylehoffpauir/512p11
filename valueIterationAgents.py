        # for every iteration, update our values
        for i in range(self.iterations):
            # create a copy of the values every iteration
            v = self.values.copy()
            for state in self.mdp.getStates():
                #every state (if not terminal) set the v dict @ state to the highest QValue action
                stateQValues = []
